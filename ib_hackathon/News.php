		Τράπεζα καινοτομεί για μία ακόμη φορά, προσφέροντας την πρώτη wearable υπηρεσία πληρωμών στην
		Ελληνική αγορά. Το νέο i-bank payband Visa αποτελεί έναν πρωτοποριακό τρόπο ανέπαφων συναλλαγών,
