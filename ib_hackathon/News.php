		Με μεγάλη επιτυχία πραγματοποιήθηκε την Πέμπτη 27 Απριλίου 2017 το Demo Day του
		προγράμματος “be finnovative”, στην έδρα του στην οδό Πειραιώς 74 στο Μοσχάτο. Το “be
		finnovative” είναι ένα πρόγραμμα επιχειρηματικής επιτάχυνσης ομάδων και νεοφυών
		επιχειρήσεων στον τομέα των χρηματο-οικονομικών συναλλαγών (fintech) και διοργανώνεται
		από την Εθνική Τράπεζα και την Crowdpolicy. <br> <br>
		Στο πλαίσιο του Demo Day, οι 9 ομάδες που συμμετείχαν στο πρόγραμμα παρουσίασαν σε
		στελέχη από venture capitals, στους εκπροσώπους του προγράμματος NBG Business Seeds,
		σε εκπροσώπους εταιρειών, συλλογικών φορέων καθώς και σε άλλους ενδιαφερόμενους, τα
		προϊόντα και τις υπηρεσίες που ανέπτυξαν κατά τη διάρκεια του 6μηνου προγράμματος.
		Οι ομάδες που ολοκλήρωσαν τον 1ο κύκλο του be finnovative είναι οι: <B> Ovolus, Buddy, Finker,
		Synaphea, Big [O] technologies, Playful.life, Echoleap, MindNodes και Yammas</B>. Οι
		εφαρμογές που ανέπτυξαν αφορούν, μεταξύ άλλων, την ψηφιοποίηση ηλεκτρονικών
		συναλλαγών και την απλούστευση διαδικασιών, την προώθηση των συναλλαγών μέσω
		κοινωνικών δικτύων, λύσεις σε blockchain και εφαρμογές σε μεγάλους οργανισμούς και στον
		πολιτισμό, τεχνολογίες chatbots, καθώς και πολυκαναλικούς τρόπους διασύνδεσης με
		πελάτες. <br> <br>
