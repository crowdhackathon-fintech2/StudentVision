		Πρόκειται για προπληρωμένη κάρτα Visa, η οποία διατίθεται σε μορφή wearable-
		βραχιολιού με ενσωματωμένη τεχνολογία ανέπαφων συναλλαγών. <br> <br>
		Το αδιάβροχο υλικό του i-bank payband Visa, καθώς και ο μοντέρνος σχεδιασμός του σε ποικιλία χρωμάτων,
		επιτρέπουν τη χρήση του παντού αντί μετρητών –από το γυμναστήριο, τη βραδινή έξοδο μέχρι και την
		παραλία– επιτρέποντας στον κάτοχό του να κάνει τις συναλλαγές του ανέπαφα, χωρίς να χρειάζεται μαζί
		του το πορτοφόλι, αφού το φορά συνέχεια στο χέρι του. <br> <br>
